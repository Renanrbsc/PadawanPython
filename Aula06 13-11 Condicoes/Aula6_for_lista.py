# Juntando listas com for

lista = ['Mateus','Matheus','Marcela','Nicole','Tonho','Pablo']
for i in range(0,6): # manipulando uma quant especifica de dados
    print(lista[i])

lista.append('Natan')
for sortudo in lista: # busca imprimir toda a lista 
    print(sortudo)

numero = 10
for i in range(0,11):
    print(f'{i} x {numero} = {i*numero}')
    


